public class endsWithString {
    public static void main(String[] args) {
        String string1 = "Hello how are you";
        System.out.println( "Hasilnya : " + string1.endsWith("u"));
        System.out.println( "Hasilnya : " + string1.endsWith("0"));
        System.out.println( "Hasilnya : " + string1.endsWith("you"));
        System.out.println( "Hasilnya : " + string1.endsWith("how"));
    }
}